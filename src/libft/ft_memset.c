#include "libft.h"

void	*ft_memset(void *a, int b, size_t c)
{
	long unsigned int		count;
	char					*a1;

	a1 = (char *)a;
	count = 0;
	while (count < c)
	{
		a1[count] = b;
		count++;
	}
	return (a);
}
