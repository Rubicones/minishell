#include "libft.h"

const char	*ft_strrchr(const char *str, int c)
{
	int			i;

	i = ft_strlen(str);
	if (ft_strlen(str) == 0 && c != '\0')
		return (0);
	if (c == '\0')
		return (&str[i]);
	while (i--)
	{
		if (str[i] == (char)c)
			return (&str[i]);
	}
	return (0);
}
